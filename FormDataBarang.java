    private void txtkodebarangCaretUpdate(javax.swing.event.CaretEvent evt) {//GEN-FIRST:event_txtkodebarangCaretUpdate
            try{
                Connection c = Classdatabase.getkoneksi();
                Statement s = c.createStatement();
                String sql = "SELECT * FROM masterbarang WHERE kodebarang = '" + this.txtkodebarang.getText() + "'";
                ResultSet r = s.executeQuery(sql);
                while (r.next()) {
                    this.txtnamabarang.setText(r.getString("namabarang"));
                    this.txthargabeli.setText(r.getString("h_beli"));
                    this.txthargajual.setText(r.getString("h_jual"));
                    this.txtstok.setText(r.getString("stok"));
                }
                r.close();
                s.close();
            } catch (SQLException e){
                JOptionPane.showMessageDialog(null, e);
            }
        
        
    }//GEN-LAST:event_txtkodebarangCaretUpdate

