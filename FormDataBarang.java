    private void btnsaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnsaveActionPerformed
        int saham1=0;
        String kodebarang = this.txtkodebarang.getText();
        String namabarang = this.txtnamabarang.getText();
        int h_beli1 = Integer.parseInt(txthargabeli.getText());
        String h_jual = this.txthargajual.getText();
        int stok1 = Integer.parseInt(txtstok.getText());
        
        saham1 = stok1*h_beli1;
        
        String h_beli = this.txthargabeli.getText();
        String stok = this.txtstok.getText();
        String saham = Integer.toString(saham1);
        
        if (txtkodebarang.getText().isEmpty()){
            JOptionPane.showMessageDialog(this, "KODE BARANG TIDAK BOLEH KOSONG");
        } else if (txtnamabarang.getText().isEmpty()){
            JOptionPane.showMessageDialog(this, "NAMA BARANG TIDAK BOLEH KOSONG");
        } else if (txthargabeli.getText().isEmpty()){
            JOptionPane.showMessageDialog(this, "HARGA BELI TIDAK BOLEH KOSONG");
        } else if (txthargajual.getText().isEmpty()){
            JOptionPane.showMessageDialog(this, "HARGA JUAL TIDAK BOLEH KOSONG");
        } else if (txtstok.getText().isEmpty()){
            JOptionPane.showMessageDialog(this, "STOK BARANG TIDAK BOLEH KOSONG");
        } else {
            
            try{
                Connection c = Classdatabase.getkoneksi();
                String sql = "INSERT INTO masterbarang VALUES (?,?,?,?,?,?)";
                PreparedStatement p = (PreparedStatement) c.prepareStatement(sql);
                p.setString(1, kodebarang);
                p.setString(2, namabarang);
                p.setString(3, h_beli);
                p.setString(4, h_jual);
                p.setString(5, stok);
                p.setString(6, saham);
                p.executeUpdate();
                p.close();
                JOptionPane.showMessageDialog(this, "Berhasil menambahkan data");
            } catch (HeadlessException | SQLException e){
                JOptionPane.showMessageDialog(null, e);
            } finally {
                
            }    
        }
        
        try {
            Connection c = Classdatabase.getkoneksi();
            Statement s = c.createStatement();
            String sql = "SELECT kodebarang, namabarang, h_beli, h_jual, stok, saham FROM masterbarang";
            ResultSet r = s.executeQuery(sql);
            tabel.setModel(DbUtils.resultSetToTableModel(r));
        } catch (SQLException e){
            JOptionPane.showMessageDialog(null, e);
        }
        
    }//GEN-LAST:event_btnsaveActionPerformed

